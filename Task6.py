# Задача 12:

# Петя и Катя – брат и сестра. Петя – студент, а Катя – школьница. Петя помогает Кате по 
# математике. Он задумывает два натуральных числа X и Y (X,Y≤1000), а Катя должна их 
# отгадать. Для этого Петя делает две подсказки. Он называет сумму этих чисел S и их 
# произведение P. 
# Помогите Кате отгадать задуманные Петей числа.

# Примечание: числа S и P задавать не нужно, они будут передаваться в тестах. 
# В результате вы должны вывести все возможные пары чисел X и Y через пробел, такие 
# что X <= Y.

# Пример:

# На входе:
# s = 12
# p = 27

# На выходе:
# 3 9


"""
Для решения этой задачи нужно перебрать все возможные пары чисел X и Y, которые 
удовлетворяют условиям X + Y = S и X * Y = P. 
Мы будем перебирать возможные значения X и вычислять Y как Y = S - X. 
Затем будем проверять, если произведение X и Y равно P, и если X <= Y, 
добавим такую пару в список результатов.
"""

s = 12
p = 27

# Список для хранения пар чисел (X, Y)
result = []

# Перебираем все возможные значения для X от 1 до s//2, так как X и Y натуральные числа
for x in range(1, s): # Перебираем все значения X от 1 до 11
    y = s - x
    if x * y == p:
        if x <= y:
            result.append((x, y)) # Метод append добавляет элемент в конец списка. В нашем случае, мы добавляем кортеж (x, y) в список result.

# Выводим все найденные пары
for pair in result:
    print(pair[0], pair[1])

"""
Этот код перебирает все возможные значения для XX и вычисляет соответствующее значение Y. 
Если X * Y = P и X <= Y, то такая пара добавляется в список result. 
В конце все найденные пары выводятся.

В результате выполнения result.append((x, y)), если все условия выполнены, список result 
будет содержать пары чисел, удовлетворяющих заданным условиям.

Эта строчка result.append((x, y)) выполняется всякий раз, когда пара чисел (x, y) 
удовлетворяет условиям задачи, добавляя эту пару в список результатов.
"""